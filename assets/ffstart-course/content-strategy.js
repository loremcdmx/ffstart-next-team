  function flow(ariaLabel, rows) {
    return {
      type: "flow",
      ariaLabel,
      steps: rows.map(function (row) {
        return { label: row[0], detail: row[1], tone: row[2] || "neutral" };
      })
    };
  }

  function compare(ariaLabel, rows) {
    return {
      type: "compare",
      ariaLabel,
      items: rows.map(function (row) {
        return { label: row[0], value: row[1], detail: row[2], tone: row[3] || "neutral" };
      })
    };
  }

  function ladder(ariaLabel, rows, max) {
    return {
      type: "ladder",
      ariaLabel,
      max: max || 100,
      items: rows.map(function (row) {
        return { label: row[0], value: row[1], display: row[2], detail: row[3], emphasis: Boolean(row[4]) };
      })
    };
  }

  function stackZones(ariaLabel, rows) {
    return {
      type: "stack-zones",
      ariaLabel,
      zones: rows.map(function (row) {
        return { label: row[0], range: row[1], detail: row[2], tone: row[3] || "neutral", weight: row[4] || 1 };
      })
    };
  }

  function odds(ariaLabel, pot, call, required, equity, note, equityLabel) {
    return { type: "odds", ariaLabel, pot, call, required, equity, note, equityLabel };
  }

